<p style="color:black;">
Islam and Christianity are the two main religions with big number of believers in Tanzania and they are the main religions in which political, public leaders and most businessmen come from.
Owing to historical reasons, whilst Christian community has been contributing greatly to the government in one hand and to its followers on the other hand, in provision of social services like education, health and water, contribution from Muslim community has always been very low. 
Furthermore, leaders from all categories in Christian community have been so close to their religious leaders as distinguished from Muslim leaders to their religion leaders.
</p>

<p style="color:black;">
On the other hand, the government which is responsible in promoting development and security to her people and maintain peace and harmony has been demanding assistance from religious leaders, who spiritually govern their followers in resolving various problems when arise. 
Again, while Christian leaders have been giving proper contribution, even by campaigning issues which seems to fit their interests through arguments and resist what contradicts their interests, official contribution from Muslim leaders has been low compared to that of Christian leaders. And in some occasions, it happen some Sheikhs and Muslims to resist issues which contradict their wellbeing by lamenting or emotionally, including calling for demonstrations to defy what seems wrong to their welfare. Such phenomenon sometimes inculcates enmity and hostility among the Muslim community itself and even between Muslims and Christians on the other hand.
</p>

<p style="color:black;">
Moreover, lack of enough knowledge on leadership and administration and being economically poor to most of Muslim leaders as they have not been considering worldly life as important, so far this has lead to improper guidance to Muslim community spiritually and physically as well.
All these have revealed leadership vacuum which can be misused and eventually mislead Muslims, the lamenting community which is about to give up. This situation gives strength the idea that Muslims are ill treated and they are not given their due rights as Christians. </p>

<p style="color:blue;font-size:14px;"><strong>1. ESTABLISHMENT OF PANEL AND ITS GOALS</strong></p>

<p style="color:black;">
What has been said in an introduction before is a real situation of Muslim community in the country which reflects Muslims life all over Africa. These are the ones who awakened some Sheikhs and made them realize weaknesses of how Muslims carry out their affairs, and therefore thought way forward to remedy this situation.
When Sheikhs and Islamic scholar mapped this situation and its ultimate consequences to Muslim community and the nation at large, they collectively came to work under one umbrella of “the Panel of Sheikhs and Islamic Scholars of Tanzania” so that they can learn how the world goes about. They intends to find out appropriate strategy to capacity building related to education, administration and economy to enable the Muslim leaders face different challenges overriding Muslim community, and that Muslims can offer desirable contribution in the development of their country.
</p>

<p style="color:black;">Therefore, the panel came to consensus that, it continues to concertize, educate themselves and do technical assessment to find right ways to remedy featuring challenges facing Muslim community, before registering an official organ which would implement the ultimate ideas after completion of this task in hand. </p>

<p style="color:black;">
On 21st June 2009 the Panel made an officially inauguration at Starlight Hotel’s hall in Dar es Salaam.  In that inaugural ceremony the panel elucidated the following key points, that the panel:</p>
<ul style="list-style:none;">
<li><blockquote>Is an independent institution which is not affiliated with any Islamic institution but may work together with all Islamic institutions in matters related to Islam and Muslims’ interests in the country.</blockquote></li>
<li><blockquote>it intends to unite Sheikhs and Islamic scholars, as experts in Islamic matters and successors of the apostles for the purpose of giving Islamic guidance in different situation in which Islam and Muslims in the country are concerned.<blockquote></li>
<li><blockquote>it will be inviting different Muslim professionals whenever areas of their expertise or their technical advices are needed. In January 2011 the said Sheikhs and Islamic Scholars registered their institution in the name of: <b>The Foundation of Sheikhs and Islamic Scholars of Tanzania</b>, which consists of the Panel of Sheikhs and Islamic Scholars, Secretariat and the Board of Trustees.</blockquote></li>
</ul>

