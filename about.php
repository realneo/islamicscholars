<p style="color:blue;font-size:14px;"><strong>DECLARATION OF INTENT :</strong></p>
<blockquote>
(a) The Foundation of Sheikhs and Islamic Scholars of Tanzania shall be informed and guided by the Qur’an and the Sunnah in all its aims, policies and procedures.
</blockquote>
<blockquote>
(b) It is an independent body working for the pleasure of Allah to promote consultation, cooperation and coordination on affairs of Sheikhs and Islamic Scholars in particular, and Muslim affairs in Tanzania, in general.
</blockquote>
<blockquote>
(c) It is a non-sectarian body working for the common good without interfering in displacing or isolating any existing Muslim work in the community.
</blockquote>
<blockquote>
(d) It is a body that will base its policies and decisions on consensus and the largest practicable measure of common agreement.
</blockquote>
<blockquote>
(e) It is a formally constituted body with a system of representation and accountability.
</blockquote>
<blockquote>
(g) It is a body that will seek to draw on the talents and specialist skills of individuals and the strengths of the organized bodies to meet the great challenges ahead in preparing the case for, and advocating, the responsibilities and rights of Sheikhs and Islamic Scholars, in particular, and Muslim Community living in Tanzania, in general.
</blockquote>

<p style="color:blue;font-size:14px;"><strong>PRINCIPAL OBJECTIVES OF THE FOUNDATION OF SHEIKHS AND ISLAMIC SCHOLARS OF TANZANIA</strong></p>
<blockquote>
i)	To unite Sheikhs and Islamic Scholars, encourage and strengthen solidarity and cooperation among them, and promote their leadership skills for the better service and benefit of the Muslim Community.
</blockquote>
<blockquote>
ii)	To establish a position for Sheikhs and Islamic Scholars, in particular, and the Muslim Community within Tanzanian Society  in general, that is fair and based on due rights.
</blockquote>
<blockquote>
iii)	To work for the eradication of disadvantages and forms of discrimination faced by Muslims.
</blockquote>
<blockquote>
iv)	To foster better community relations and work for the good of Tanzanian Society as a whole.
</blockquote>
<blockquote>
v)	To establish, manage, maintain and develop Religious Schools, Nursery and Primary schools, Secondary schools, Vocational Training Centers, Children’s centers, Kindergartens, Medical Centres, Orphanages and other Islamic Social Projects and activities.
</blockquote>
<blockquote>
vi)	To own and run bookshops, printing press, newspapers, magazines, and any other kind of Islamic literature for the purpose of disseminating Islamic knowledge.
</blockquote>
<blockquote>
vii)	To cooperate with any other Islamic Centres, Institutions, Organizations, Associations in the country or outside the country for the purpose of advancing Islam.
</blockquote>
<blockquote>
viii)	To receive or give grants, donations, aids, loans, Zakat from or to similar organizations, governments, National and International for the purpose of promoting Islam.
</blockquote>
<blockquote>
ix)	To purchase, take on lease or in exchange, hire or otherwise acquire and hold temporarily or permanently and to dispose off any other movable or immovable properties.
</blockquote>
<blockquote>
x)	To engage in any business falling under Islamic jurisdiction for the purpose of generating funds for financing its activities.
</blockquote>
<blockquote>
xi)	To impart Islamic knowledge to Muslims this can create environment for them to adapt in practice Islamic way of life.
</blockquote>   
